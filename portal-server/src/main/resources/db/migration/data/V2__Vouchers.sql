-- TYPE: BY_TIME, BY_DATA, UNKNOWN
-- STATUS: READY, UNAVAILABLE, DELETED, ON_CONFIG
-- CURRENCY: FREE, USD, ARS
-- UNIT: MINUTE, HOUR, DAY, MEGAS, GIGAS

CREATE TABLE VOUCHERS (
    ID INT UNSIGNED NOT NULL AUTO_INCREMENT,
    NAME VARCHAR(32) NOT NULL,
    TYPE VARCHAR(32) NOT NULL,
    STATUS VARCHAR(32) DEFAULT 'ON_CONFIG' NOT NULL,
    CURRENCY VARCHAR(6) NOT NULL,
    PRICE DECIMAL(9,3) NOT NULL DEFAULT 0,

    UNIT VARCHAR(32) DEFAULT NULL,
    DATA_INT INT SIGNED DEFAULT NULL,

    CREATION_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    MODIFICATION_TIME TIMESTAMP DEFAULT NULL,
    DELETION_TIME TIMESTAMP DEFAULT NULL,

    PRIMARY KEY(ID),
    UNIQUE KEY (NAME)
);
